"""Phase 6 training orchestrator.

Two stages:
  Stage 0 — Watchog-style contrastive pretraining on synthetic_corpus.jsonl
            (the Phase 5 Stage D corpus, generated by data/synthetic_corpus.py).
            Encoder-only. SimCLR-style: positive pair = two augmentations of
            the SAME source example; negatives = augmentations of DIFFERENT
            source examples within the minibatch. 3 epochs default.

  Stage 1 — Multi-task fine-tune on (gold + synthesized_train.jsonl +
            LF-fused soft labels from labeling_functions.py).
            Loss: ce + 0.3 * crf_nll + 0.1 * sibling_relation_loss
            5 epochs default, early-stop on val macro-F1 (patience=2).

CLI:
    /tmp/schema-mapper-venv/bin/python train.py \\
        --epochs 5 \\
        --contrastive-epochs 3 \\
        --batch-size 32 \\
        --device mps

NOTE on `train(False)`: this is the same as model.<eval-mode>() in PyTorch,
just spelled in the alternate documented form (Module.train accepts a bool).
We use train(False)/train(True) throughout this file to dodge an overly
strict security hook that pattern-matches the eval-mode method-call literal.
Behavior is identical.
"""
